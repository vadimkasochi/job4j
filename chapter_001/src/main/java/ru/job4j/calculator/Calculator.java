package ru.job4j.calculator;

public class Calculator {

    public double add(double first, double second) {
        return first + second;
    }
    public double vachel(double first, double second) {
        return first - second;
    }
    public double umn(double first, double second) {
        return first * second;
    }
    public double razdel(double first, double second) {
        return first / second;
    }
}